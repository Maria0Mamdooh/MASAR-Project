import time

def load_test(model, query_dataset, index, device, batch_sizes=[1, 4, 8, 16, 32]):
    model.eval()
    results = {}

    for bs in batch_sizes:
        imgs = torch.stack([query_dataset[i][0] for i in range(min(bs, len(query_dataset)))]).to(device)

        # Warmup
        with torch.no_grad():
            _ = model.query_net(imgs)

        # Timed run
        times = []
        for _ in range(10):
            start = time.time()
            with torch.no_grad():
                feats = model.query_net(imgs)
                feats = F.normalize(feats, dim=1).cpu().numpy().astype("float32")
                index.search(feats, 10)
            times.append(time.time() - start)

        avg_time = np.mean(times)
        per_image = avg_time / bs
        results[bs] = {"total_ms": avg_time * 1000, "per_image_ms": per_image * 1000}
        print(f"  Batch {bs:2d} | Total: {avg_time*1000:.1f}ms | Per image: {per_image*1000:.2f}ms")

    return results

print("=== Load Test: Inference Latency ===")
load_results = load_test(model, query_dataset, index, device)

