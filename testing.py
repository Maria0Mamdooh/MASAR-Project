import torch
import torch.nn.functional as F
import torchvision.transforms.functional as TF
import numpy as np
import matplotlib.pyplot as plt

def add_gaussian_noise(img_tensor, std=0.5):  #From 10% to 30% to 50%
    noise = torch.randn_like(img_tensor) * std
    return torch.clamp(img_tensor + noise, 0, 1)

def add_blur(img_tensor, kernel_size=21): #From 5 to 11 to 21
    from torchvision.transforms import GaussianBlur
    blur = GaussianBlur(kernel_size=kernel_size, sigma=2.0)
    return blur(img_tensor)

def add_brightness(img_tensor, factor=4.0): #From 1.8 to 2.5 to 4.0
    return torch.clamp(img_tensor * factor, 0, 1)

def stress_test(model, query_dataset, gallery_features, gallery_labels, index, device, n_samples=50):
    model.eval()
    results = {"clean": 0, "noise": 0, "blur": 0, "brightness": 0}

    for i in range(n_samples):
        img, true_label, _ = query_dataset[i]
        img = img.to(device)

        def get_top1_label(tensor):
            feat = model.query_net(tensor.unsqueeze(0))
            feat = F.normalize(feat, dim=1).detach().cpu().numpy().astype("float32")  # ← added .detach()
            _, idxs = index.search(feat, 1)
            return gallery_labels[idxs[0][0]]

        # Clean
        if get_top1_label(img) == true_label:
            results["clean"] += 1

        # Noise
        noisy = add_gaussian_noise(img.cpu()).to(device)
        if get_top1_label(noisy) == true_label:
            results["noise"] += 1

        # Blur
        blurred = add_blur(img.cpu()).to(device)
        if get_top1_label(blurred) == true_label:
            results["blur"] += 1

        # Brightness
        bright = add_brightness(img.cpu()).to(device)
        if get_top1_label(bright) == true_label:
            results["brightness"] += 1

    for k in results:
        results[k] = results[k] / n_samples

    return results

stress_results = stress_test(model, query_dataset, gallery_features, gallery_labels, index, device, n_samples=50)

print("=== Stress Test Results (Recall@1) ===")
for condition, score in stress_results.items():
    print(f"  {condition.capitalize():12s}: {score:.4f}")

