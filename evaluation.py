def measure_single_inference_time(model, query_dataset, index, device):
    model.eval()

    img, label, path = query_dataset[0]
    img = img.unsqueeze(0).to(device)

    start_time = time.time()

    with torch.no_grad():
        feature = model.query_net(img)
        feature = F.normalize(feature, dim=1)

    feature_np = feature.cpu().numpy().astype("float32")

    scores, indices = index.search(feature_np, 10)

    end_time = time.time()

    inference_time = end_time - start_time

    return inference_time, label, indices[0], scores[0]

