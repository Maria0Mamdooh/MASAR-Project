"""GradCAM-based saliency for TransGeo (ViT backbone).

    WHY NOT vanilla gradients:
      Vanilla gradients are computed w.r.t. the raw pixel tensor. In a ViT the
      image is first split into patches, then each patch is projected into a
      high-dimensional token, then all tokens are mixed by attention 12 times.
      By the time you backprop to the pixels, the spatial information has been
      averaged away — you get a uniform, blurry heatmap.

    HOW GRADCAM FIXES IT:
      Instead of going all the way back to the pixels, we stop at the OUTPUT of
      the last transformer block. At that point the model still has one token per
      image patch (e.g. 16×16 = 256 tokens for a 256-px image). Each token
      still "knows" which region of the image it came from. Gradients there are
      strong and spatially meaningful.

    STEPS:
      1. Hook the last ViT block to capture activations + gradients.
      2. Forward: compute cosine similarity between drone and satellite features.
      3. Backward: get gradients at the hooked layer.
      4. Weight each patch-token's activation by its mean gradient.
      5. Sum → ReLU → reshape to 2-D grid → upsample to 256×256.

    Returns
    -------
    saliency : np.ndarray shape [H, W], values in [0, 1]
"""

#1. Saliency Map — GradCAM

def generate_saliency_map(model, query_img, gallery_img, device):
    from scipy.ndimage import gaussian_filter

    model.eval()
    query_b   = query_img.unsqueeze(0).to(device)
    gallery_b = gallery_img.unsqueeze(0).to(device)

    blocks       = _find_vit_blocks(model)
    target_block = blocks[-1]
    activations, gradients = {}, {}

    def fwd_hook(module, inp, out):
        activations["v"] = out

    def bwd_hook(module, grad_in, grad_out):
        gradients["v"] = grad_out[0]

    fh = target_block.register_forward_hook(fwd_hook)
    bh = target_block.register_full_backward_hook(bwd_hook)

    with torch.no_grad():
        gallery_feat = model.reference_net(x=gallery_b)
        gallery_feat = F.normalize(gallery_feat, dim=1)

    query_feat = model.query_net(query_b)
    query_feat = F.normalize(query_feat, dim=1)
    score      = (query_feat * gallery_feat).sum()

    for p in model.parameters():
        if p.grad is not None:
            p.grad.zero_()
    score.backward()

    fh.remove()
    bh.remove()

    acts = activations["v"].detach()[:, 1:, :]
    grds = gradients["v"].detach()[:, 1:, :]

    weights   = grds.mean(dim=1, keepdim=True)
    cam       = F.relu((weights * acts).sum(dim=2))  # [1, N_patches]

    #FIX: ensure that number of tokens perfect square
    n = cam.shape[1]
    grid_side = int(n ** 0.5)
    n_keep = grid_side * grid_side
    cam = cam[:, :n_keep]                   #remove extra token
    # ────────────────────────────────────────────────

    cam = cam.reshape(1, 1, grid_side, grid_side)

    H      = query_b.shape[2]
    cam_up = F.interpolate(cam, size=(H, H), mode="bilinear", align_corners=False)
    sal    = cam_up.squeeze().cpu().numpy()
    sal    = gaussian_filter(sal, sigma=8)
    lo, hi = np.percentile(sal, 2), np.percentile(sal, 98)
    sal    = np.clip(sal, lo, hi)
    sal    = (sal - sal.min()) / (sal.max() - sal.min() + 1e-8)

    return sal

