def occlusion_feature_importance(model, query_img, gallery_img, device, grid_size=4):
    from scipy.ndimage import gaussian_filter

    model.eval()
    query_b   = query_img.unsqueeze(0).to(device)
    gallery_b = gallery_img.unsqueeze(0).to(device)

    with torch.no_grad():
        gallery_feat    = model.reference_net(x=gallery_b)
        gallery_feat    = F.normalize(gallery_feat, dim=1)
        query_feat_orig = model.query_net(query_b)
        query_feat_orig = F.normalize(query_feat_orig, dim=1)
        original_score  = torch.matmul(query_feat_orig, gallery_feat.T).item()

    _, C, H, W = query_b.shape
    patch_h    = H // grid_size
    patch_w    = W // grid_size
    imp_map    = np.zeros((grid_size, grid_size), dtype=np.float32)

    for i in range(grid_size):
        for j in range(grid_size):
            h0, h1   = i * patch_h, (i + 1) * patch_h
            w0, w1   = j * patch_w, (j + 1) * patch_w
            occluded = query_b.clone()
            local_mean = occluded[:, :, h0:h1, w0:w1].mean(dim=[2, 3], keepdim=True)
            occluded[:, :, h0:h1, w0:w1] = local_mean

            with torch.no_grad():
                occ_feat  = F.normalize(model.query_net(occluded), dim=1)
                occ_score = torch.matmul(occ_feat, gallery_feat.T).item()

            imp_map[i, j] = original_score - occ_score

    imp_t  = torch.tensor(imp_map).unsqueeze(0).unsqueeze(0)
    imp_up = F.interpolate(imp_t, size=(H, W), mode="bicubic", align_corners=False).squeeze().numpy()
    imp_up = gaussian_filter(imp_up, sigma=12)
    lo, hi = np.percentile(imp_up, 5), np.percentile(imp_up, 95)
    imp_up = np.clip(imp_up, lo, hi)
    imp_up = (imp_up - imp_up.min()) / (imp_up.max() - imp_up.min() + 1e-8)

    return imp_up, original_score

